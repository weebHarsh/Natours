// QUERY MIDDLEWARE: if we just use find in our hook it only queries the document for find but not for findOne, findByID, findByIDAndUpdate, etc
// SO TO FIX THAT we use regular expressions to select all find operation which starts from find using /^find/
tourSchema.pre(/^find/, function(next) {
  this.find({ secretTour: { $ne: true } });

  this.start = Date.now();
  next();
});

tourSchema.pre(/^find/, function(next) {
  this.populate({
    path: 'guides',
    select: '-__v -passwordChangedAt'
  });

  next();
});

tourSchema.post(/^find/, function(docs, next) {
  console.log(`Query took ${Date.now() - this.start} milliseconds`);
  // console.log(docs);
  next();
});

